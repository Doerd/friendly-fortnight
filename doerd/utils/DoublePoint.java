package doerd.utils;

public class DoublePoint {
	private double x;
	private double y;
	public DoublePoint(double c1, double c2){
		this.x = c1;
		this.y = c2;
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
}
